
# Inteiro
x = 15
print(x, type(x))

# Float
preco = 19.99
print(preco, type(preco))

# Strings funcionão para aspas duplas e simples
cidade1 = "São Paulo"
cidade2 = 'Rio de Janeiro'
print(cidade1, cidade2, type(cidade1), type(cidade2))

# Booleanos
disponivel = True
print(disponivel, type(disponivel))

disponivel = False
print(disponivel, type(disponivel))